function vec = zero2nan( vec )
% ZERO2NAN(vec) sets all of the zeros in vec to NaN
%--------------------------------------------------------------------------
% ARGUMENTS
% vec       A vector.
%--------------------------------------------------------------------------
% OUTPUT
% 
%--------------------------------------------------------------------------
% EXAMPLES
% zero2nan([1,5.1,0, 0, 0.0001])
%--------------------------------------------------------------------------
% AUTHOR: Sam Davenport.

vec(abs(vec)<=2*eps) = nan;

end

